package ch04;

public class BinaryTest {
	
	public static void main(String[] args) {
		
		int num = 10;
		int bNun = 0B1010; // B뒤로 2진수//
		int oNum = 012;  //맨 앞에 0을 붙여사용//
		int xNum = 0XA; //16진수는 앞에 X사용 A는 10을 나타냄//
		
		System.out.println(num);
		System.out.println(bNun);
		System.out.println(oNum);
		System.out.println(xNum);
		
	}
}
